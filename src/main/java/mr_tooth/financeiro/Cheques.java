    public void PreencherJtable(){
        DefaultTableModel Modelo = (DefaultTableModel)jTableConsultas.getModel();
        Modelo.setNumRows(0);

        try{
            while(con_cheq.rs.next()){
                Modelo.addRow(new Object [] {
                    con_cheq.rs.getString("cod_cheque"),
                    con_cheq.rs.getString("nome_pac"),
                    con_cheq.rs.getString("nometitular_cheq"),
                    con_cheq.rs.getString("valor_cheq"),
                    con_cheq.rs.getString("banco_cheq"),
                    con_cheq.rs.getString("agencia_cheq"),
                    con_cheq.rs.getString("conta_cheq"),
                    con_cheq.rs.getString("compensacao_cheq"),
                    con_cheq.rs.getString("status_cheq")
                });
            }
        } 
        catch(Exception erroTable){
            JOptionPane.showMessageDialog(null,"Erro ao carregar cheques cadastrados","ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
