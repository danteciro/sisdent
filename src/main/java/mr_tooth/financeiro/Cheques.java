    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int LinhaSelecionada = this.jTableConsultas.getSelectedRow();
        String CodigoCheque = this.jTableConsultas.getValueAt(LinhaSelecionada, 0).toString();

        try{
            //Busca no banco de dados o código do paciente selecionado para ser excluido
            String ChequeSelecionado = "SELECT * FROM cheques WHERE cod_cheque = "+CodigoCheque;
            con_cheq.QuerySQL(ChequeSelecionado);
            con_cheq.rs.next();

            //Mensagem de confirmação
            String MsgConfirmacao = "Deseja realmente excluir o cheque recebido do paciente: \n "+con_cheq.rs.getString("nome_pac")+" ?";
            int OpcaoEscolhida = JOptionPane.showConfirmDialog(null, MsgConfirmacao, "Excluir", JOptionPane.YES_NO_OPTION);

            if(OpcaoEscolhida == JOptionPane.YES_OPTION){
                //Excluindo a consulta agendada após a confirmação positiva
                String ExcluirConsulta = "DELETE FROM cheques WHERE cod_cheque = "+CodigoCheque;
                int ExcluirSucesso  = con_cheq.stmt.executeUpdate(ExcluirConsulta);

                if(ExcluirSucesso == 1){
                    JOptionPane.showMessageDialog(null,"Cheque excluido com sucesso!","Excluir!", JOptionPane.INFORMATION_MESSAGE);
                    //Atualiza jTable
                    PreencherJtable();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Não foi possível excluir o cheque!", "Erro!", JOptionPane.ERROR_MESSAGE);
                    //Atualiza jTable
                    //PreencherJtable();
                }
            }
            else{
                //Atualiza jTable
                PreencherJtable();
                return;
            }
        }
        catch(Exception erroSQL){
             JOptionPane.showMessageDialog(null,"Erro ao excluir", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

