    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int LinhaSelecionada = this.jTableConsultas.getSelectedRow();
        String CodigoConsulta = this.jTableConsultas.getValueAt(LinhaSelecionada, 0).toString();

        try{
            //Busca no banco de dados o código do paciente selecionado para ser excluido
            String ConsultaSelecionado = "SELECT * FROM agenda WHERE cod_agenda = '"+CodigoConsulta+"'";
            con_agen.QuerySQL(ConsultaSelecionado);
            con_agen.rs.next();

            //Mensagem de confirmação
            String MsgConfirmacao = "Deseja realmente cancelar a consulta do paciente: \n "+con_agen.rs.getString("nome_pac")+" ?";
            int OpcaoEscolhida = JOptionPane.showConfirmDialog(null, MsgConfirmacao, "Excluir", JOptionPane.YES_NO_OPTION);

            if(OpcaoEscolhida == JOptionPane.YES_OPTION){
                //Excluindo a consulta agendada após a confirmação positiva
                String ExcluirConsulta = "DELETE FROM agenda WHERE cod_agenda = "+CodigoConsulta;
                int ExcluirSucesso  = con_agen.stmt.executeUpdate(ExcluirConsulta);

                if(ExcluirSucesso == 1){
                    JOptionPane.showMessageDialog(null,"Consulta cancelada com sucesso!","Excluir!", JOptionPane.INFORMATION_MESSAGE);
                    //Atualiza jTable
                    PreencherJtable();
                }
                else{                    
                    //Atualiza jTable
                    PreencherJtable();
                    return;
                }
            }
            else{
                //Atualiza jTable
                PreencherJtable();
                return;
            }
        }
        catch(Exception erroSQL){
             JOptionPane.showMessageDialog(null,"Erro ao excluir", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

