    public void PreencherJtable(){
        DefaultTableModel Modelo = (DefaultTableModel)jTableConsultas.getModel();
        Modelo.setNumRows(0);

        try{
            while(con_agen.rs.next()){
                Modelo.addRow(new Object [] {
                    con_agen.rs.getString("cod_agenda"),
                    con_agen.rs.getString("data_agen"),
                    con_agen.rs.getString("hora_agen"),
                    con_agen.rs.getString("nome_pac"),
                    con_agen.rs.getString("nome_dent"),
                    con_agen.rs.getString("observacao_agen"),
                    con_agen.rs.getString("nome_tra"),
                    con_agen.rs.getString("faltou_agen"),
                });
            }
        } 
        catch(Exception erroTable){
            JOptionPane.showMessageDialog(null,"Erro ao carregar consultas agendadas","ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
