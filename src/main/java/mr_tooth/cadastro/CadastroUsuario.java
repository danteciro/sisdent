    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String codigoUsuario = txtCodigo.getText();
        try{
            //Busca no banco de dados o código do usuário selecionado para ser excluido
            String UsuarioSelecionado = "SELECT * FROM usuarios WHERE cod_usuario = '"+codigoUsuario+"'";
            con_usuario.QuerySQL(UsuarioSelecionado);
            con_usuario.rs.next();

            //Mensagem de confirmação
            String MsgConfirmacao = "Deseja realmente excluir o usuário: "+con_usuario.rs.getString("login_us")+" ?";
            int OpcaoEscolhida = JOptionPane.showConfirmDialog(null, MsgConfirmacao, "Excluir", JOptionPane.YES_NO_OPTION);

            if(OpcaoEscolhida == JOptionPane.YES_OPTION){
                //Excluindo o usuário após a confirmação positiva
                String ExcluirUsuario = "DELETE FROM usuarios WHERE cod_usuario = '"+codigoUsuario+"'";
                int ExcluirSucesso  = con_usuario.stmt.executeUpdate(ExcluirUsuario);

                if(ExcluirSucesso == 1){
                    JOptionPane.showMessageDialog(null,"Usuário excluido com sucesso!","Excluir!", JOptionPane.INFORMATION_MESSAGE);
                    //Atualiza ResultSet
                    AtualizaRs();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Não foi possível excluir o usuário!", "Erro!", JOptionPane.ERROR_MESSAGE);
                    //Atualiza ResultSet
                    AtualizaRs();
                }
            }
            else{
                //Atualiza ResultSet
                AtualizaRs();
                return;
            }
        }
        catch(Exception erroSQL){
             JOptionPane.showMessageDialog(null,"Erro ao excluir", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

