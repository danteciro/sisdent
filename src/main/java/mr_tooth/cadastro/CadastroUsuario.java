    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        try{
            con_usuario.rs.first();
            String Igual = "nao";
            int TamanhoPesquisa = txtPesquisa.getText().length();

            while(Igual == "nao"){
                String Pesquisado = con_usuario.rs.getString("nome_us").substring(0,(TamanhoPesquisa));
                if(Pesquisado.equals(txtPesquisa.getText())){
                    Igual = "sim";
                }
                else{
                    con_usuario.rs.next();
                }
            }
            //Mostra os dados pesquisados
            txtCodigo.setText(con_usuario.rs.getString("cod_usuario")); //Codigo do usuario pesquisado
            txtNome.setText(con_usuario.rs.getString("nome_us")); //Nome do usuario pesquisado
            txtUsuario.setText(con_usuario.rs.getString("login_us")); //Login do usuario pesquisado
            txtSenha.setText(con_usuario.rs.getString("senha_us")); //Senha do usuario pesquisado
            cbTipo.setSelectedItem(con_usuario.rs.getString("tipo_us")); //Tipo do usuario pesquisado
        }
        catch(Exception erroPesquisa){
            JOptionPane.showMessageDialog(null,"Não foi possivel localizar este usuário", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPesquisaActionPerformed

