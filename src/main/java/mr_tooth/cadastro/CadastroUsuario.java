    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try{
            String nome    = txtNome.getText();
            String usuario = txtUsuario.getText();
            String senha   = txtSenha.getText();

            //Verifica no banco de dados se já existe algum usuário usando o login desejado
            String UsuarioLogin = "SELECT * FROM usuarios WHERE login_us = '"+usuario+"'";
            con_usuario.QuerySQL(UsuarioLogin);

            if(nome.length() < 3){
                JOptionPane.showMessageDialog(null,"Por favor, digite o seu nome!","Atenção", JOptionPane.WARNING_MESSAGE);
            }
            else if(usuario.length() < 3){
                JOptionPane.showMessageDialog(null,"Por favor, digite o seu nome de usuário!","Atenção", JOptionPane.WARNING_MESSAGE);
            }
            else if(usuario.length() > 16){
                JOptionPane.showMessageDialog(null,"O seu nome de usuário, não pode ter mais que 16 caracteres!","Atenção", JOptionPane.WARNING_MESSAGE);
            }
             else if(con_usuario.rs.next()){
                JOptionPane.showMessageDialog(null,"O nome de usuário desejado já está sendo usado!","Atenção", JOptionPane.WARNING_MESSAGE);
            }
            else if(senha.equals("")){
                JOptionPane.showMessageDialog(null,"Por favor, digite o sua senha!","Atenção", JOptionPane.WARNING_MESSAGE);

            }
            else if(senha.length() > 8){
                JOptionPane.showMessageDialog(null,"A sua senha, não pode ter mais que 8 caracteres","Atenção", JOptionPane.WARNING_MESSAGE);
            }
            else{
                //Inseri os dados no banco
                String UsuarioInsert = "INSERT INTO usuarios () VALUES (NULL,'"+nome+"','"+usuario+"','"+senha+"','"+cbTipo.getSelectedItem()+"')";
                con_usuario.stmt.execute(UsuarioInsert);
                JOptionPane.showMessageDialog(null,"Usuário cadastrado com sucesso!","Cadastrado!", JOptionPane.INFORMATION_MESSAGE);
                //Atualiza ResultSet
                AtualizaRs();
            }
        }
        catch(SQLException erroSQL){
             JOptionPane.showMessageDialog(null,"Não foi possível cadastrar o usuário!", "Erro!", JOptionPane.ERROR_MESSAGE);
             //Atualiza ResultSet
             AtualizaRs();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

